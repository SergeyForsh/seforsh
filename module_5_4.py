class House:
    houses_history = []  # Атрибут класса для хранения истории построек

    def __new__(cls, *args, **kwargs):
        # Добавляем название дома в историю
        if args:
            cls.houses_history.append(args[0])  # args[0] - это название дома
        return super(House, cls).__new__(cls)

    def __init__(self, name, floors):
        self.name = name  # Название дома
        self.floors = floors  # Количество этажей

    def __del__(self):
        # Выводим сообщение о сносе дома
        print(f"{self.name} снесён, но он останется в истории")


# Пример использования
h1 = House('ЖК Эльбрус', 10)
print(House.houses_history)
h2 = House('ЖК Акация', 20)
print(House.houses_history)
h3 = House('ЖК Матрёшки', 20)
print(House.houses_history)
# Удаление объектов
del h2
del h3
print(House.houses_history)
del h1  # Удаляем первый объект

### Объяснение кода:

# 1. **Атрибут класса `houses_history`**:
#    - Это список, который будет хранить названия всех построенных домов.
# 2. **Метод `__new__`**:
#    - Этот метод вызывается перед `__init__`. Здесь мы добавляем название дома
# (которое передается как первый позиционный аргумент в `args`) в `houses_history`.
# 3. **Метод `__init__`**:
#    - Этот метод инициализирует экземпляр класса. Он присваивает название и количество этажей дому.
# 4. **Метод `__del__`**:
#    - Этот метод вызывается при удалении объекта. Он выводит сообщение о сносе дома,
# но при этом название остается в истории.



