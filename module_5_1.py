class House:
    def __init__(self, name, number_of_floors):
        self.name = name
        self.number_of_floors = number_of_floors

    def go_to(self, new_floor):
        if 1 <= new_floor <= self.number_of_floors:
            for floor in range(1, new_floor + 1):
                print(floor)
        else:
            print("Такого этажа не существует")

# Примеры использования
h1 = House('ЖК Горский', 18)
h2 = House('Домик в деревне', 2)

h1.go_to(5)  # Выводит этажи от 1 до 5
h2.go_to(10) # Выводит "Такого этажа не существует"

### Объяснение кода:

# 1. **Класс House**: Мы создаем класс `House`, который содержит метод `__init__` для инициализации объектов.
#
# 2. **Метод `__init__`**: Этот метод принимает два параметра: `name` (имя дома) и
# `number_of_floors` (количество этажей). Эти параметры присваиваются атрибутам объекта
# `self.name` и `self.number_of_floors`.
#
# 3. **Метод `go_to`**: Этот метод принимает `new_floor` (номер этажа, на который необходимо подняться).
#    - Если `new_floor` находится в пределах от 1 до `self.number_of_floors`,
# программа выводит номера этажей от 1 до `new_floor`.
#    - Если `new_floor` вне этого диапазона, выводится сообщение "Такого этажа не существует".
#
# 4. **Примеры использования**: Создаются два объекта класса `House`,
# и вызывается метод `go_to` для каждого из них с разными параметрами.